/** @param {string} id */
export function findCommand(id) {
  const key = id.trim().toLowerCase();
  return COMMAND_REGISTRY.find(
    (row) =>
      row.id.toLowerCase() === key ||
      row.cliAlias?.toLowerCase() === key ||
      row.path.toLowerCase().endsWith(`/${key}`) ||
      row.path.toLowerCase().endsWith(`\\${key}`),
  );
}
